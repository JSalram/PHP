    /**
     * @param null $id
     * @return int|mixed|string
     * @throws \Doctrine\ORM\NoResultException
     * @throws \Doctrine\ORM\NonUniqueResultException
     */
    public function buscarId($id = null)
    {
        return $this->getEntityManager()
            ->createQuery(
                "
                SELECT p 
                FROM App\Entity\Producto p
                WHERE p.id =:id
                "
            )
            ->setParameter("id", $id)
            ->getOneOrNullResult();
    }

